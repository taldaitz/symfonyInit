<?php

namespace App\Entity;

use App\Repository\ArticleRepository;
use Doctrine\ORM\Mapping as ORM;
use Symfony\Component\Validator\Constraints as Assert;

/**
 * @ORM\Entity(repositoryClass=ArticleRepository::class)
 */
class Article
{
    /**
     * @ORM\Id
     * @ORM\GeneratedValue
     * @ORM\Column(type="integer")
     */
    private $id;

    /**
     * @ORM\Column(type="string", length=255)
     */
    private $title;

    /**
     * @ORM\Column(type="date")
     * Assert\LessThan("today")
     */
    private $date;

    /**
     * @ORM\Column(type="text", nullable=true)
     * Assert\Length(min = 200)
     * Assert\NotBlank
     */
    private $content;

    /**
     * @ORM\Column(type="string", length=50)
     * Assert\Choice({"publié", "en cours de rédaction", "annulé"})
     */
    private $status;

    /**
     * @ORM\ManyToOne(targetEntity=Auteur::class, inversedBy="articles")
     * @ORM\JoinColumn(nullable=false)
     */
    private $writer;

    public function getId(): ?int
    {
        return $this->id;
    }

    public function getTitle(): ?string
    {
        return $this->title;
    }

    public function setTitle(string $title): self
    {
        $this->title = $title;

        return $this;
    }

    public function getDate(): ?\DateTimeInterface
    {
        return $this->date;
    }

    public function setDate(\DateTimeInterface $date): self
    {
        $this->date = $date;

        return $this;
    }

    public function getContent(): ?string
    {
        return $this->content;
    }

    public function setContent(?string $content): self
    {
        $this->content = $content;

        return $this;
    }

    public function getStatus(): ?string
    {
        return $this->status;
    }

    public function setStatus(string $status): self
    {
        $this->status = $status;

        return $this;
    }

    public function getWriter(): ?Auteur
    {
        return $this->writer;
    }

    public function setWriter(?Auteur $writer): self
    {
        $this->writer = $writer;

        return $this;
    }
}
